"""
Модель инерциальной навигационной системы (ИНС).

ИНС измеряет:
    - ω (угловая скорость рыскания, рад/с) — гироскоп
    - a (линейное ускорение, м/с²) — акселерометр

ИНС не является датчиком с матрицей H, так как она поставляет
управляющие сигналы (control input), а не прямые измерения состояния.
Поэтому ИНС обрабатывается особым образом: её данные подаются
в шаг predict() фильтра, а не в update().

Частота: 10 Гц (равна частоте основного цикла фильтра, Δt = 0.1 с).

Модель шумов ИНС (раздел 2.3):
    Реальная МЭМС-ИНС имеет два компонента погрешности:

    1. Белый шум (случайное блуждание, ARW для гироскопа):
       Для гироскопа σ_gyro = 0.05 рад/с ≈ 2.9°/с.
       Для акселерометра σ_accel = 0.1 м/с².
       Эти значения типичны для навигационного класса МЭМС-ИМУ
       (например, серии Analog Devices ADIS16490).

    2. Дрейф нуля гироскопа (bias instability):
       Гироскоп имеет медленно меняющееся смещение, которое
       моделируется как случайное блуждание (random walk):
           bias(k+1) = bias(k) + N(0, σ_bias_drift)
       где σ_bias_drift = 0.0001 рад/с на шаг Δt.
       Это приводит к накоплению ошибки курса, даже если
       средний шум = 0. Именно дрейф гироскопа — главная причина
       того, что ИНС без коррекции «уплывает» за минуты.

    Зачем это важно для диплома:
       Без дрейфа (только белый шум) ошибка ИНС нереалистично мала,
       т.к. белый шум частично компенсируется при интегрировании.
       Дрейф — систематическая ошибка — не компенсируется и растёт
       пропорционально √t. Это делает задачу коррекции (EKF)
       по-настоящему необходимой.

Рекомендации по тестированию:
    - Проверить, что get_control() возвращает [ω, a] правильной размерности.
    - Проверить, что среднее шума ≈ 0 на большой выборке (без дрейфа).
    - Проверить, что дрейф гироскопа накапливается со временем.
    - Проверить, что СКО шума ≈ заявленному значению.
"""

import numpy as np


class INS:
    """Модель ИНС (гироскоп + акселерометр) навигационного класса МЭМС.

    ИНС предоставляет зашумлённые управляющие сигналы [ω, a],
    которые используются на этапе predict() фильтра.

    Модель включает:
        - Белый гауссов шум (случайная компонента)
        - Дрейф нуля гироскопа (bias drift) — медленно меняющееся
          смещение, моделируемое как случайное блуждание

    Простыми словами:
        Каждый раз, когда фильтр запрашивает управление, ИНС
        возвращает «истинное значение + шум + накопленный дрейф».
        Дрейф медленно «блуждает» от шага к шагу, создавая
        систематическую ошибку, которую EKF должен компенсировать
        по показаниям внешних датчиков (DVL, Compass, USBL).

    Args:
        gyro_noise_std: СКО белого шума гироскопа (рад/с).
            Для МЭМС навигационного класса ≈ 0.05 рад/с (раздел 2.3).
        accel_noise_std: СКО белого шума акселерометра (м/с²).
            Для МЭМС навигационного класса ≈ 0.1 м/с² (раздел 2.3).
        gyro_bias_drift_std: СКО приращения дрейфа гироскопа
            на один шаг (рад/с / шаг). По умолчанию 0.0001 рад/с.
        rate: Частота обновления ИНС (Гц).
    """

    def __init__(
        self,
        gyro_noise_std: float = 0.05,
        accel_noise_std: float = 0.1,
        gyro_bias_drift_std: float = 0.0001,
        rate: float = 10.0,
    ):
        self._gyro_noise_std = gyro_noise_std
        self._accel_noise_std = accel_noise_std
        self._gyro_bias_drift_std = gyro_bias_drift_std
        self._rate = rate
        self._name = "INS"

        # Текущий дрейф гироскопа (начинается с нуля)
        self._gyro_bias: float = 0.0

    def get_control(self, true_omega: float, true_accel: float) -> np.ndarray:
        """Генерация зашумлённого управляющего сигнала.

        Модель ошибки гироскопа (раздел 2.3):
            ω_noisy = ω_true + bias + N(0, σ_gyro)
            bias(k+1) = bias(k) + N(0, σ_bias_drift)

        Модель ошибки акселерометра:
            a_noisy = a_true + N(0, σ_accel)

        Args:
            true_omega: Истинная угловая скорость (рад/с).
            true_accel: Истинное ускорение (м/с²).

        Returns:
            Зашумлённый вектор управления [ω_noisy, a_noisy].
        """
        # Обновляем дрейф гироскопа (случайное блуждание)
        self._gyro_bias += np.random.normal(0, self._gyro_bias_drift_std)

        # Гироскоп: истинное + дрейф + белый шум
        omega_noisy = (
            true_omega
            + self._gyro_bias
            + np.random.normal(0, self._gyro_noise_std)
        )

        # Акселерометр: истинное + белый шум
        accel_noisy = true_accel + np.random.normal(0, self._accel_noise_std)

        return np.array([omega_noisy, accel_noisy])

    def reset_bias(self) -> None:
        """Сброс дрейфа гироскопа (для нового сценария)."""
        self._gyro_bias = 0.0

    @property
    def gyro_bias(self) -> float:
        """Текущий дрейф гироскопа (рад/с)."""
        return self._gyro_bias

    @property
    def rate(self) -> float:
        """Частота обновления (Гц)."""
        return self._rate

    @property
    def name(self) -> str:
        """Имя датчика."""
        return self._name
