"""
Модель доплеровского лага (DVL).

DVL измеряет скорость НПА относительно грунта (раздел 2.3).
Измерение: v (скорость, м/с) — индекс 2 вектора состояния.

Матрица наблюдения: H = [0, 0, 1, 0]
Частота: 5 Гц.

Суммарный шум DVL включает температурную погрешность (раздел 2.3):
    σ_DVL = 0.03 м/с  (базовая 0.02 + температурная 0.01 м/с)

При отклонении температуры от номинальных 20°C на ±5°C
дополнительная погрешность увеличивает σ_V на 0.01 м/с.
Для наихудшего случая используется суммарное σ_DVL = 0.03 м/с.

Рекомендации по тестированию:
    - measure() при v=1.5 м/с должен возвращать значение ≈ 1.5 ± шум.
    - H имеет размер (1 × 4) и выбирает только скорость.
    - СКО измерений ≈ 0.03 м/с (с учётом температуры).
"""

import numpy as np
from sensors.base_sensor import BaseSensor


class DVL(BaseSensor):
    """Доплеровский лаг — измеряет скорость НПА.

    Шум включает температурную погрешность: σ_DVL = 0.03 м/с
    (базовая 0.02 + температурная 0.01 м/с, раздел 2.3).

    Args:
        noise_std: Суммарное СКО шума скорости (м/с).
            По умолчанию 0.03 — с учётом температурной погрешности.
        rate: Частота обновления (Гц).
    """

    def __init__(self, noise_std: float = 0.03, rate: float = 5.0):
        super().__init__(
            noise_std=noise_std,
            rate=rate,
            state_dim=4,
            name="DVL",
        )

    def get_H(self) -> np.ndarray:
        """Матрица наблюдения: выбирает скорость (индекс 2).

        Returns:
            H = [[0, 0, 1, 0]] размером (1 × 4).
        """
        H = np.zeros((1, self._state_dim))
        H[0, 2] = 1.0  # v
        return H
